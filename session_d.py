from pathlib import Path
import re

app = Path("/workspaces/spx-mesh-editor/src/App.jsx")
src = app.read_text()
original_len = len(src.splitlines())

# ── 1. State vars ─────────────────────────────────────────────────────────────
old_state = "  const [showProbePanel, setShowProbePanel] = useState(false);"
new_state = (
    "  const [showProbePanel, setShowProbePanel] = useState(false);\n"
    "  const [showUDIMPanel, setShowUDIMPanel] = useState(false);\n"
    "  const [udimLayout, setUdimLayout] = useState(null);\n"
    "  const [udimTileCount, setUdimTileCount] = useState(4);\n"
    "  const [udimActiveTile, setUdimActiveTile] = useState(1001);\n"
    "  const [udimPaintColor, setUdimPaintColor] = useState('#00ffc8');\n"
    "  const [giResolution, setGiResolution] = useState(256);\n"
    "  const [giProbes, setGiProbes] = useState([]);\n"
    "  const [giBaked, setGiBaked] = useState(false);"
)
src = src.replace(old_state, new_state, 1)

# ── 2. Handlers ───────────────────────────────────────────────────────────────
old_handler = "  // ── Sessions 108-121: VFX + Fluid + Asset + Procedural state ─────────────"
new_handler = (
    "  // ── Session D: GI + UDIM handlers ──────────────────────────────────────\n"
    "  const handleBakeGI = useCallback(() => {\n"
    "    const renderer = rendererRef.current;\n"
    "    const scene    = sceneRef.current;\n"
    "    const camera   = cameraRef.current;\n"
    "    if (!renderer || !scene || !camera) { setStatus('No renderer'); return; }\n"
    "    setStatus('Baking GI...');\n"
    "    const envMap = bakeEnvironment(renderer, scene, camera, { resolution: giResolution });\n"
    "    if (envMap) {\n"
    "      applyIBLToScene(scene, envMap, { intensity: 1.0, background: false });\n"
    "      const probe = createIrradianceProbe(new THREE.Vector3(0,0,0), { resolution: giResolution });\n"
    "      setGiProbes(p => [...p, probe]);\n"
    "      setGiBaked(true);\n"
    "      setStatus('GI baked');\n"
    "    } else { setStatus('GI bake failed'); }\n"
    "  }, [giResolution]);\n"
    "\n"
    "  const handleCreateReflectionProbe = useCallback(() => {\n"
    "    const renderer = rendererRef.current;\n"
    "    const scene    = sceneRef.current;\n"
    "    const camera   = cameraRef.current;\n"
    "    if (!renderer || !scene || !camera) return;\n"
    "    const probe = createReflectionProbe(new THREE.Vector3(0,1,0), { resolution: giResolution });\n"
    "    updateReflectionProbe(probe, renderer, scene);\n"
    "    applyProbeToScene(scene, probe);\n"
    "    setGiProbes(p => [...p, probe]);\n"
    "    setStatus('Reflection probe created');\n"
    "  }, [giResolution]);\n"
    "\n"
    "  const handleInitUDIM = useCallback(() => {\n"
    "    const layout = createUDIMLayout(udimTileCount);\n"
    "    initUDIMLayout(layout, 1024, 1024);\n"
    "    setUdimLayout(layout);\n"
    "    setStatus('UDIM initialized: ' + udimTileCount + ' tiles');\n"
    "  }, [udimTileCount]);\n"
    "\n"
    "  const handleFillUDIMTile = useCallback(() => {\n"
    "    if (!udimLayout) { setStatus('Init UDIM first'); return; }\n"
    "    fillUDIMTile(udimLayout, udimActiveTile, udimPaintColor);\n"
    "    setUdimLayout({ ...udimLayout });\n"
    "    setStatus('Filled tile ' + udimActiveTile);\n"
    "  }, [udimLayout, udimActiveTile, udimPaintColor]);\n"
    "\n"
    "  const handleExportUDIMAtlas = useCallback(() => {\n"
    "    if (!udimLayout) { setStatus('Init UDIM first'); return; }\n"
    "    const atlas = buildUDIMAtlas(udimLayout, 4096, 4096);\n"
    "    const url   = atlas.canvas.toDataURL('image/png');\n"
    "    const a     = document.createElement('a');\n"
    "    a.href = url; a.download = 'udim_atlas.png'; a.click();\n"
    "    setStatus('UDIM atlas exported');\n"
    "  }, [udimLayout]);\n"
    "\n"
    "  const handleApplyUDIMToMesh = useCallback(() => {\n"
    "    const mesh = meshRef.current;\n"
    "    if (!mesh || !udimLayout) { setStatus('Need mesh + UDIM layout'); return; }\n"
    "    applyUDIMToMaterial(mesh.material, udimLayout);\n"
    "    setStatus('UDIM applied to mesh');\n"
    "  }, [udimLayout]);\n"
    "  // ── Session D END ─────────────────────────────────────────────────────\n"
    "  // ── Sessions 108-121: VFX + Fluid + Asset + Procedural state ─────────────"
)
src = src.replace(old_handler, new_handler, 1)

