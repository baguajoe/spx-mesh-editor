    "              border:'1px solid #21262d',color:'#aaa',borderRadius:3,cursor:'pointer',fontSize:8,marginBottom:3}}>\n"
    "              Apply to Mesh\n"
    "            </button>\n"
    "            <button onClick={handleExportUDIMAtlas} style={{width:'100%',padding:'4px',background:'#1a1f2e',\n"
